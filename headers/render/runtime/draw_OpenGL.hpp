// A namespace used for functions associated with the main run loop.
namespace runtime
{
  // for drawing OpenGL data
  void draw_OpenGL()
  {
    // choose VAO and VBO
    glBindVertexArray(global::vertexArrayObject);

    // draw
    glDrawElements
    (
      GL_TRIANGLES,         // shape
      36,                    // number of vertices drawn to (count repeats)
      GL_UNSIGNED_INT,      // data type
      0                     // offset into index array for first element (triangle vertex order)
    );
  }
}